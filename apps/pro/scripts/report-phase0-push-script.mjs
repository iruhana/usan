import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const appRoot = process.cwd()
const repoRoot = resolve(appRoot, '..', '..')
const outputDir = resolve(appRoot, 'output', 'phase0-readiness')
const pushHandoffPath = resolve(outputDir, 'phase0-push-handoff.json')
const commitHandoffPath = resolve(outputDir, 'phase0-commit-handoff.json')
const jsonOutputPath = resolve(outputDir, 'phase0-push-script.json')
const markdownOutputPath = resolve(outputDir, 'phase0-push-script.md')
const powerShellOutputPath = resolve(outputDir, 'phase0-push-sequence.ps1')

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'))
}

function psSingleQuote(value) {
  return `'${String(value).replaceAll("'", "''")}'`
}

function buildPowerShellScript(report) {
  const lines = [
    '[CmdletBinding(SupportsShouldProcess=$true, ConfirmImpact=\'Medium\')]',
    'param(',
    '  [switch]$SkipSimulate,',
    '  [switch]$SkipStage,',
    '  [switch]$SkipPublishStatus,',
    '  [switch]$SkipCommitDryRun,',
    '  [switch]$SkipEvidenceManifest,',
    '  [switch]$SkipBundleEvidence,',
    '  [switch]$SkipBundleVerify,',
    '  [switch]$SkipPublishReadiness,',
    '  [switch]$SkipCommit,',
    '  [switch]$SkipPush,',
    '  [switch]$SkipObserve,',
    '  [switch]$SkipCloseout,',
    '  [string]$ObserveRunId',
    ')',
    '',
    '$ErrorActionPreference = \'Stop\'',
    'Set-StrictMode -Version Latest',
    '',
    `$RepoRoot = ${psSingleQuote(report.repoRoot)}`,
    `$AppRoot = ${psSingleQuote(report.appRoot)}`,
    '$Phase0OutputDir = Join-Path $AppRoot \'output\\phase0-readiness\'',
    `$CommitMessagePath = ${psSingleQuote(report.commitMessagePath)}`,
    `$DefaultObserveRef = ${psSingleQuote(report.branchName)}`,
    '$NpmCommand = if (Get-Command npm.cmd -ErrorAction SilentlyContinue) { \'npm.cmd\' } else { \'npm\' }',
    '',
    'function Invoke-NativeCommand {',
    '  param(',
    '    [string]$WorkingDirectory,',
    '    [string]$FilePath,',
    '    [string[]]$ArgumentList,',
    '    [string]$DisplayCommand',
    '  )',
    '',
    '  Push-Location $WorkingDirectory',
    '  try {',
    '    Write-Host ">> $DisplayCommand"',
    '    & $FilePath @ArgumentList',
    '    if ($LASTEXITCODE -ne 0) {',
    '      throw "Command failed with exit code ${LASTEXITCODE}: $DisplayCommand"',
    '    }',
    '  } finally {',
    '    Pop-Location',
    '  }',
    '}',
    '',
    'function Invoke-WriteStep {',
    '  param(',
    '    [string]$Target,',
    '    [string]$Action,',
    '    [scriptblock]$ScriptBlock',
    '  )',
    '',
    '  if ($PSCmdlet.ShouldProcess($Target, $Action)) {',
    '    & $ScriptBlock',
    '    return $true',
    '  }',
    '',
    '  return $false',
    '}',
    '',
    'function Read-Phase0JsonReport {',
    '  param(',
    '    [string]$ReportPath',
    '  )',
    '',
    '  if (-not (Test-Path -LiteralPath $ReportPath)) {',
    '    throw "Expected Phase 0 report not found: $ReportPath"',
    '  }',
    '',
    '  return Get-Content -LiteralPath $ReportPath -Raw | ConvertFrom-Json',
    '}',
    '',
    'function Assert-Phase0ReportStatus {',
    '  param(',
    '    [string]$ReportPath,',
    '    [string]$Context,',
    '    [string[]]$AllowedStatuses,',
    '    [string[]]$TrueProperties = @()',
    '  )',
    '',
    '  $report = Read-Phase0JsonReport -ReportPath $ReportPath',
    '  $status = [string]$report.status',
    '',
    '  if ($AllowedStatuses.Count -gt 0 -and $AllowedStatuses -notcontains $status) {',
    '    throw "$Context expected status [$($AllowedStatuses -join \', \')] but got [$status] from $ReportPath"',
    '  }',
    '',
    '  foreach ($propertyName in $TrueProperties) {',
    '    $propertyValue = $report.$propertyName',
    '    if ($propertyValue -ne $true) {',
    '      throw "$Context expected $propertyName=true in $ReportPath but got [$propertyValue]"',
    '    }',
    '  }',
    '}',
    '',
    '$SimulatePublishReportPath = Join-Path $Phase0OutputDir \'phase0-simulate-publish.json\'',
    '$PublishStatusReportPath = Join-Path $Phase0OutputDir \'phase0-publish-status.json\'',
    '$CommitDryRunReportPath = Join-Path $Phase0OutputDir \'phase0-commit-dry-run.json\'',
    '$EvidenceManifestReportPath = Join-Path $Phase0OutputDir \'phase0-evidence-manifest.json\'',
    '$BundleEvidenceReportPath = Join-Path $Phase0OutputDir \'phase0-bundle-evidence.json\'',
    '$BundleVerifyReportPath = Join-Path $Phase0OutputDir \'phase0-bundle-verify.json\'',
    '$PublishReadinessReportPath = Join-Path $Phase0OutputDir \'phase0-publish-readiness.json\'',
    '$CloseoutReportPath = Join-Path $Phase0OutputDir \'phase0-closeout.json\'',
    '$StageExecuted = $false',
    '$CommitExecuted = $false',
    '$PushExecuted = $false',
    '$ObserveExecuted = $false',
    '',
    'Invoke-NativeCommand -WorkingDirectory $RepoRoot -FilePath \'git\' -ArgumentList @(',
    '  \'-C\', $RepoRoot, \'status\', \'--short\', \'--branch\'',
    ') -DisplayCommand "git -C `"$RepoRoot`" status --short --branch"',
    '',
    'Invoke-NativeCommand -WorkingDirectory $RepoRoot -FilePath \'git\' -ArgumentList @(',
    '  \'-C\', $RepoRoot, \'diff\', \'--stat\', \'--\', \'.github/workflows/pro-quality.yml\', \'apps/pro\', \'ISSUES.md\'',
    ') -DisplayCommand "git -C `"$RepoRoot`" diff --stat -- .github/workflows/pro-quality.yml apps/pro ISSUES.md"',
    '',
    'if (-not $SkipSimulate) {',
    '  Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '    \'run\', \'phase0:simulate-publish\'',
    '  ) -DisplayCommand "$NpmCommand run phase0:simulate-publish"',
    '  Assert-Phase0ReportStatus -ReportPath $SimulatePublishReportPath -Context \'phase0:simulate-publish\' -AllowedStatuses @(\'phase0-simulate-publish-ready\') -TrueProperties @(\'simulatedReady\')',
    '}',
    '',
    'if (-not $SkipStage) {',
    '  $StageExecuted = Invoke-WriteStep -Target $RepoRoot -Action \'Stage Phase 0 scope\' -ScriptBlock {',
    '    Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '      \'run\', \'phase0:stage-scope\', \'--\', \'--apply\'',
    '    ) -DisplayCommand "$NpmCommand run phase0:stage-scope -- --apply"',
    '  }',
    '}',
    '',
    'if (-not $SkipPublishStatus) {',
    '  Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '    \'run\', \'phase0:publish-status\'',
    '  ) -DisplayCommand "$NpmCommand run phase0:publish-status"',
    '  $publishStatusAllowed = if ($StageExecuted -or $SkipStage) { @(\'phase0-publish-ready\') } else { @(\'phase0-publish-ready\', \'phase0-publish-not-ready\') }',
    '  $publishStatusProperties = if ($StageExecuted -or $SkipStage) { @(\'readyToCommit\') } else { @() }',
    '  Assert-Phase0ReportStatus -ReportPath $PublishStatusReportPath -Context \'phase0:publish-status\' -AllowedStatuses $publishStatusAllowed -TrueProperties $publishStatusProperties',
    '}',
    '',
    'if (-not $SkipCommitDryRun) {',
    '  Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '    \'run\', \'phase0:commit-dry-run\'',
    '  ) -DisplayCommand "$NpmCommand run phase0:commit-dry-run"',
    '  $commitDryRunAllowed = if ($StageExecuted -or $SkipStage) { @(\'phase0-commit-ready\') } else { @(\'phase0-commit-ready\', \'phase0-commit-not-ready\') }',
    '  $commitDryRunProperties = if ($StageExecuted -or $SkipStage) { @(\'readyToCommit\') } else { @() }',
    '  Assert-Phase0ReportStatus -ReportPath $CommitDryRunReportPath -Context \'phase0:commit-dry-run\' -AllowedStatuses $commitDryRunAllowed -TrueProperties $commitDryRunProperties',
    '}',
    '',
    'if (-not $SkipEvidenceManifest) {',
    '  Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '    \'run\', \'phase0:evidence-manifest\'',
    '  ) -DisplayCommand "$NpmCommand run phase0:evidence-manifest"',
    '  Assert-Phase0ReportStatus -ReportPath $EvidenceManifestReportPath -Context \'phase0:evidence-manifest\' -AllowedStatuses @(\'phase0-evidence-manifest-ready\', \'phase0-evidence-manifest-local-ready-remote-pending\')',
    '}',
    '',
    'if (-not $SkipBundleEvidence) {',
    '  Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '    \'run\', \'phase0:bundle-evidence\'',
    '  ) -DisplayCommand "$NpmCommand run phase0:bundle-evidence"',
    '  Assert-Phase0ReportStatus -ReportPath $BundleEvidenceReportPath -Context \'phase0:bundle-evidence\' -AllowedStatuses @(\'phase0-bundle-ready\', \'phase0-bundle-local-ready-remote-pending\')',
    '}',
    '',
    'if (-not $SkipBundleVerify) {',
    '  Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '    \'run\', \'phase0:bundle-verify\'',
    '  ) -DisplayCommand "$NpmCommand run phase0:bundle-verify"',
    '  Assert-Phase0ReportStatus -ReportPath $BundleVerifyReportPath -Context \'phase0:bundle-verify\' -AllowedStatuses @(\'phase0-bundle-verify-ready\', \'phase0-bundle-verify-local-ready-remote-pending\')',
    '}',
    '',
    'if (-not $SkipPublishReadiness) {',
    '  Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '    \'run\', \'phase0:publish-readiness\'',
    '  ) -DisplayCommand "$NpmCommand run phase0:publish-readiness"',
    '  $publishReadinessAllowed = if ($StageExecuted -or $SkipStage) { @(\'phase0-publish-ready-for-remote\', \'phase0-publish-complete\') } else { @(\'phase0-publish-ready-for-remote\', \'phase0-publish-complete\', \'phase0-publish-prepared-local-only\') }',
    '  Assert-Phase0ReportStatus -ReportPath $PublishReadinessReportPath -Context \'phase0:publish-readiness\' -AllowedStatuses $publishReadinessAllowed',
    '}',
    '',
    'if (-not $SkipCommit) {',
    '  $CommitExecuted = Invoke-WriteStep -Target $RepoRoot -Action \'Create Phase 0 closeout commit\' -ScriptBlock {',
    '    Invoke-NativeCommand -WorkingDirectory $RepoRoot -FilePath \'git\' -ArgumentList @(',
    '      \'-C\', $RepoRoot, \'commit\', \'-F\', $CommitMessagePath',
    '    ) -DisplayCommand "git -C `"$RepoRoot`" commit -F `"$CommitMessagePath`""',
    '  }',
    '}',
    '',
    'if (-not $SkipPush) {',
    '  $PushExecuted = Invoke-WriteStep -Target $RepoRoot -Action \'Push Phase 0 branch\' -ScriptBlock {',
    '    Invoke-NativeCommand -WorkingDirectory $RepoRoot -FilePath \'git\' -ArgumentList @(',
    '      \'-C\', $RepoRoot, \'push\', \'origin\', $DefaultObserveRef',
    '    ) -DisplayCommand "git -C `"$RepoRoot`" push origin $DefaultObserveRef"',
    '  }',
    '}',
    '',
    '$observeArgs = if ($ObserveRunId) {',
    '  @(\'run\', \'phase0:ci-observe\', \'--\', \'--run-id\', $ObserveRunId)',
    '} else {',
    '  @(\'run\', \'phase0:ci-observe\', \'--\', \'--ref\', $DefaultObserveRef)',
    '}',
    '$observeDisplay = if ($ObserveRunId) {',
    '  "$NpmCommand run phase0:ci-observe -- --run-id $ObserveRunId"',
    '} else {',
    '  "$NpmCommand run phase0:ci-observe -- --ref $DefaultObserveRef"',
    '}',
    '',
    'if (-not $SkipObserve) {',
    '  $ObserveExecuted = Invoke-WriteStep -Target $DefaultObserveRef -Action \'Observe remote Phase 0 CI run\' -ScriptBlock {',
    '    Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList $observeArgs -DisplayCommand $observeDisplay',
    '  }',
    '}',
    '',
    'if (-not $SkipCloseout) {',
    '  Invoke-NativeCommand -WorkingDirectory $AppRoot -FilePath $NpmCommand -ArgumentList @(',
    '    \'run\', \'phase0:closeout\'',
    '  ) -DisplayCommand "$NpmCommand run phase0:closeout"',
    '  if ($ObserveExecuted) {',
    '    Assert-Phase0ReportStatus -ReportPath $CloseoutReportPath -Context \'phase0:closeout\' -AllowedStatuses @(\'phase0-complete\') -TrueProperties @(\'remoteConfirmationReady\')',
    '  }',
    '}',
  ]

  return `${lines.join('\n')}\n`
}

function buildMarkdown(report) {
  const lines = [
    '# Phase 0 Push Script',
    '',
    `- Generated at: ${report.generatedAt}`,
    `- Repo root: ${report.repoRoot}`,
    `- App root: ${report.appRoot}`,
    `- Branch: ${report.branchName}`,
    `- PowerShell script: ${report.powerShellScriptPath}`,
    '',
    '## Suggested Usage',
    `- Review only: powershell -ExecutionPolicy Bypass -File ${report.powerShellScriptPath} -WhatIf`,
    `- Execute full flow: powershell -ExecutionPolicy Bypass -File ${report.powerShellScriptPath} -Confirm:$false`,
    `- Reuse an existing CI run: powershell -ExecutionPolicy Bypass -File ${report.powerShellScriptPath} -SkipSimulate -SkipStage -SkipCommit -SkipPush -ObserveRunId <run-id> -Confirm:$false`,
    `- Skip local bundle/publish checks: powershell -ExecutionPolicy Bypass -File ${report.powerShellScriptPath} -SkipEvidenceManifest -SkipBundleEvidence -SkipBundleVerify -SkipPublishReadiness -Confirm:$false`,
    '',
    '## Generated Steps',
    '- status and diff review',
    '- phase0:simulate-publish',
    '- phase0:stage-scope -- --apply',
    '- phase0:publish-status',
    '- phase0:commit-dry-run',
    '- phase0:evidence-manifest',
    '- phase0:bundle-evidence',
    '- phase0:bundle-verify',
    '- phase0:publish-readiness',
    '- git commit -F phase0-commit-message.txt',
    '- git push origin <branch>',
    '- phase0:ci-observe',
    '- phase0:closeout',
  ]

  return `${lines.join('\n')}\n`
}

mkdirSync(outputDir, { recursive: true })

if (!existsSync(pushHandoffPath)) {
  throw new Error('Missing phase0-push-handoff.json. Run `npm run phase0:push-handoff` first.')
}

if (!existsSync(commitHandoffPath)) {
  throw new Error('Missing phase0-commit-handoff.json. Run `npm run phase0:commit-handoff` first.')
}

const pushHandoff = readJson(pushHandoffPath)
const commitHandoff = readJson(commitHandoffPath)
const report = {
  status: 'phase0-push-script-ready',
  generatedAt: new Date().toISOString(),
  repoRoot,
  appRoot,
  branchName: pushHandoff.branchName ?? 'main',
  commitMessagePath: commitHandoff.commitMessagePath ?? resolve(outputDir, 'phase0-commit-message.txt'),
  pushHandoffPath,
  commitHandoffPath,
  powerShellScriptPath: powerShellOutputPath,
}

const powerShellScript = buildPowerShellScript(report)
writeFileSync(powerShellOutputPath, powerShellScript)
writeFileSync(jsonOutputPath, `${JSON.stringify(report, null, 2)}\n`)
writeFileSync(markdownOutputPath, buildMarkdown(report))

console.log(`Phase 0 push script written to ${powerShellOutputPath}`)
